a = input('Enter a ')
b = input('Enter b ')
a1 = int(a)
b1 = int(b)

print(f'a is {a1} \tb is {b1}')
print(f'Sum ({a1}+{b1}) is {a1 + b1}')  # addition using + operator
print(f'Diff ({a1}-{b1}) is {a1 - b1}')  # substraction using - operator
print(f'Mul ({a1}*{b1}) is {a1 * b1}')  # multiplication using * operator
print(f'Div ({a1}/{b1}) is {a1 / b1}')  # division using / operator
print(f'Mod ({a1}%{b1}) is {a1 % b1}')  # modulus using % operator
print(f'Div with // ({a1}//{b1}) is {a1 // b1}')  # floor division using // operator
print(f'a power b ({a1}**{b1}) is {a1 ** b1}')  # exponent using ** operator
