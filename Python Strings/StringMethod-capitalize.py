# capitalize() string method
s1 = 'welcome to python Programming'
print(s1.capitalize())
'''
Output:
Welcome to python programming
'''

s2 = 'Welcome to python programming'
print(s2.capitalize())
'''
Output:
Welcome to python programming
'''

s3 = '12-welcome to python programming'
print(s3.capitalize())
'''
Output:
12-welcome to python programming
'''

s4 = '$$welcome to python programming'
print(s4.capitalize())
'''
Output:
$$welcome to python programming
'''