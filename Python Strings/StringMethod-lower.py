# lower() string method
s1 = '## WELCOME TO PYTHON PROGRAMMING ##'
print(s1.lower())
'''
Output:
## welcome to python programming ##
'''

s2 = 'Welcome to Python Programming'
print(s2.lower())
'''
Output:
welcome to python programming
'''

s3 = 'welcome to python programming'
print(s3.lower())
'''
Output:
welcome to python programming
'''