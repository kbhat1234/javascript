# sqrt(x) math method
import math as m
from math import sqrt, pow, factorial
print(f'sqrt({16}) is {sqrt(16)}')
'''
Output:
sqrt(16) is 4.0
'''

# mixed
print(f'sqrt(pow({2}, {2}) is {sqrt(pow(2, 2))}')
'''
Output:
sqrt(pow(2, 2) is 2.0
'''

print(f'sqrt(pow(factorial({3}), {2}) is {sqrt(pow(factorial(3), 2))}')
'''
Output:
sqrt(pow(factorial(3), 2) is 6.0
'''